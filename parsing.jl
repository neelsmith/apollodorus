function buildparser()
	rulesds = joinpath(pwd() |> dirname, "Kanones.jl", "datasets", "literarygreek-rules") 
	vocabds = joinpath(pwd() |> dirname, "Kanones.jl", "datasets", "lsj-vocab") 
